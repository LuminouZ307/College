#pragma once

int penjumlahan(int x, int y) {
	int jumlah = x + y;
	return jumlah;
}

int pengurangan(int x, int y) {
	int kurang = x - y;
	return kurang;
}

int perkalian(int x, int y) {
	int kali = x * y;
	return kali;
}

int pembagian(float x, float y) {
	float bagi = x / y;
	return bagi;
}

int sisabagi(int x, int y) {
	int modulus = x % y;
	return modulus;
}