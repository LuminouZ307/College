#include <iostream>

using namespace std;

int penjumlahan(int x, int y) {
    return x + y;
}

int pengurangan(int x, int y) {
    return x - y;
}

int perkalian(int x, int y) {
    return x * y;
}

float pembagian(float x, float y) {
    return x / y;
}

int sisabagi(int x, int y) {
    return x % y;
}
